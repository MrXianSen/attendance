using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Attendance.MODEL.Model
{
    /// <summary>
    /// 部门信息
    /// </summary>
    public class Department
    {
        public Department() 
        {

        }

        private string _DepID;
        private string _DepName;

        /// <summary>
        /// 部门ID
        /// </summary>
        public string DepID
        {
            get { return _DepID; }
            set { _DepID = value; }
        }
        
        /// <summary>
        /// 部门名称
        /// </summary>
        public string DepName
        {
            get { return _DepName; }
            set { _DepName = value; }
        }
    }
}