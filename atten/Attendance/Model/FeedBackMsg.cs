using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Attendance.Model
{
    public class FeedBackMsg<T>
    {
        private int _Code;
        private string _Msg;
        private T _Obj;
        public int Code
        {
            get { return _Code; }
            set { _Code = value; }
        }
        public string Msg
        {
            get { return _Msg; }
            set { _Msg = value; }
        }
        public T Obj
        {
            get { return _Obj; }
            set { _Obj = value; }
        }
    }
}
